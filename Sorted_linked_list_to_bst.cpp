class Solution{
  public:
   TNode* tt(vector<int> &vc,int low,int high)
 {
     if(low>high)
       return nullptr;
         int mid=low+(high-low)/2;
        if((low+high) & 1) mid += 1;
        TNode *root=new TNode(vc[mid]); 
        root->left=tt(vc,low,mid-1);
        root->right=tt(vc,mid+1,high);
       return root;
 }
   TNode* sortedListToBST(LNode *head) {
       //code here
       vector<int> vc;
       while(head!=nullptr)
       {
           vc.push_back(head->data);
           head=head->next;
       }
       TNode* head1=tt(vc,0,vc.size()-1);
       return head1;
   }
};

